public class Main {
    public static void main(String[] args) {
       int apple = 4;
       System.out.println(apple);
        float mango = 4f;
        System.out.println(mango);
        boolean banana=true ;
        System.out.println(banana);
        char fruits ='h';
        System.out.println(fruits);
        String para = "hello \"pakistan\" hello"; // we use this if we want to print double coma so we use selash
        System.out.println(para);
        String parat = "hello \\pakistan\\ hello"; // we use this if we want to print selash so we use selash
        System.out.println(parat);
        String parap = "hello pakistan\n hello"; // we use this if we want to print next string from new line
        System.out.println(parap);
    }
}